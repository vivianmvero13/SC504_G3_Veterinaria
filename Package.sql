            SELECT 1
            FROM FIDE_USUARIOS_TB
            WHERE USUARIO_CORREO = P_CORREO
              AND USUARIO_CONTRASENNA = P_CONTRASENNA;
    
        V_EXISTE NUMBER := 0;
    BEGIN
